func (db DB) UserTableGet() ([]User, error) {
	rows, err := db.pool.Query(context.Background(), "select * from users;")
	if err != nil {
		return nil, err
	}
	users, err := pgx.CollectRows(rows, pgx.RowToStructByName[User])
	return users, err
}

